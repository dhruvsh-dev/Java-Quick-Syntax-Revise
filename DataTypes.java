import java.util.ArrayList;

class MyClass {
    // simple empty class for demonstration
}

public class DataTypes {
    public static void main(String[] args) {

        // Primitive Data Types (Syntax)
        byte b = 10;
        short s = 200;
        int num = 5000;
        long count = 100000L;

        float pi = 3.14f;
        double rate = 99.99;
        char letter = 'A';
        boolean isActive = true;

        // Non-Primitive Data Types (Syntax)
        String name = "Dhruv";

        int[] numbers = {1, 2, 3, 4};

        ArrayList<String> list = new ArrayList<>();

        MyClass obj = new MyClass();

        int[][] matrix = new int[3][3];   // Multi-dimensional array
    }
}
